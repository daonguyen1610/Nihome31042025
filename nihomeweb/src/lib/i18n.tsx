type Ctx = {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: keyof typeof dict | string) => string;
};

const I18nContext = createContext<Ctx | null>(null);

export const I18nProvider = ({ children }: { children: ReactNode }) => {
  const [lang, setLangState] = useState<Lang>(() => {
    const saved = typeof window !== "undefined" ? (localStorage.getItem("nicon_lang") as Lang) : null;
    return saved && ["vi", "en", "zh", "ja"].includes(saved) ? saved : "vi";
  });

  useEffect(() => {
    localStorage.setItem("nicon_lang", lang);
    document.documentElement.lang = lang;
  }, [lang]);

  const setLang = (l: Lang) => setLangState(l);
  const t = (key: string) => {
    const entry = dict[key];
    if (!entry) return key;
    // Fallback chain: requested lang → en → vi → key
    return entry[lang] ?? entry.en ?? entry.vi ?? key;
  };

  return <I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>;
};

export const useI18n = () => {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
};
